//1. JS program to add two numbers
function add(num1,num2){
    return num1+num2;
}
//2. JS program to find remainder
function remainder(num1,num2){
    return num1%num2;
}
//3. JS program to comapre two variables
function compare(a,b){
    return a>b;
}
//4. square of a number
function square(num){
    return num*num;
}
//5. average
function average(num1,num2,num3,num4,num5){
    return (num1+num2+num3+num4+num5)/5
}